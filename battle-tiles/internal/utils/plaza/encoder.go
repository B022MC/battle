const (
	SocketVersion = 0x1
)

type Encoder struct {
	sendRound        byte
	recvRound        byte
	sendXorKey       uint32
	recvXorKey       uint32
	sendPackageCount uint
}

func (e *Encoder) Decrypt(data []byte) (*game.Packer, error) {
	packer := &game.Packer{}
	if err := packer.Load(data); err != nil {
		return nil, err
	}

	if err := e.doDecrypt(packer); err != nil {
		return nil, err
	}

	return packer, nil
}

func (e *Encoder) genKey() uint32 {
	seed := uint32(0x4AE84505)
	a := uint32(0x26A6ED)
	b := uint32(0x3ADCF) //TODO 这里应该是一个随机数
	key := uint32(0xA57CA55A)

	xorKey := (a + uint32(uint16(seed))*b) >> 0x10
	xorKey ^= key
	return xorKey
}

func (e *Encoder) Encrypt(packer *game.Packer) []byte {
	//调整长度,保证字（32bit)对齐
	rawSize := packer.BufferSize()
	adjustedSize := rawSize
	if rawSize%4 != 0 {
		adjustedSize = 4 - rawSize%4
		adjustedSize += rawSize
	}

	// 字节映射
	checkCode := byte(0)
	for i := 4; i < rawSize; i++ { //这里也是跳过了cmdInfo结构
		checkCode += packer.ReadByte(i)
		newIndex := packer.ReadByte(i) + e.sendRound
		newByte := sendMap[newIndex]

		packer.WriteByte(i, newByte)
		e.sendRound += 3
	}

	//创建秘钥
	xorKey := e.sendXorKey
	if e.sendPackageCount == 0 {
		xorKey = e.genKey() //for debug only
		e.sendXorKey = xorKey
		e.recvXorKey = xorKey
	}

	//执行加密
	for i := 4; i < adjustedSize; i += 4 {
		result := packer.ReadDWord(i) ^ xorKey
		packer.WriteDWord(i, result)

		a := uint32(0x26A6ED)
		b := uint32(0x3ADCF)

		low := uint32(uint16(result))
		high := uint32(uint16(result >> 0x10))

		low = (a + low*b) >> 0x10
		high = (a + high*b) >> 0x10

		key := high<<0x10 | low
		xorKey = key ^ 0xA55AA55A
	}

	if e.sendPackageCount == 0 { //插入秘钥,只有在发送第一个包的时候才插入秘钥给服务器,这个是大端插入的
		packer.InsertDWord(8, e.sendXorKey)
	}

	//填写信息头
	packer.WriteByte(0, SocketVersion)
	packer.WriteByte(1, ^checkCode+1)
	packer.WriteWord(2, uint16(packer.BufferSize())) //数据长是整个buffer的长度

	//设置变量
	e.sendPackageCount++
	e.sendXorKey = xorKey

	return packer.Bytes()
}

func (e *Encoder) doDecrypt(packer *game.Packer) error {
	//调整长度
	rawSize := packer.BufferSize()
	adjustedSize := rawSize
	if rawSize%4 != 0 {
		adjustedSize = 4 - rawSize%4
		adjustedSize += rawSize
	}
	snapSize := adjustedSize - rawSize
	count := adjustedSize/4 - 1

	//解密
	startOffset := 4
	xorKey := e.recvXorKey
	for i := startOffset; i < adjustedSize; i += 4 {
		if snapSize > 0 && i/4 == count {
			var mask uint32
			for t := 0; t < snapSize; t++ {
				shift := 24 - t*8
				mask = uint32(0xff << shift)
				v := byte((xorKey & mask) >> shift)
				packer.WriteByte(packer.BufSize+snapSize-1-t, v)
			}
		}

		raw := packer.ReadDWord(i)
		packer.WriteDWord(i, raw^xorKey)

		a := uint32(0x26A6ED)
		b := uint32(0x3ADCF)

		low := uint32(uint16(raw))
		high := uint32(uint16(raw >> 0x10))

		low = (a + low*b) >> 0x10
		high = (a + high*b) >> 0x10

		key := high<<0x10 | low
		xorKey = key ^ 0xA55AA55A
	}

	//字节映射
	for i := startOffset; i < rawSize; i++ { //这里也是跳过了cmdInfo结构
		newIndex := packer.ReadByte(i)
		newByte := recvMap[newIndex] - e.recvRound
		packer.WriteByte(i, newByte)
		e.recvRound += 3
	}

	packer.Head.Cmd.MainCmdID = packer.ReadWord(4)
	packer.Head.Cmd.SubCmdID = packer.ReadWord(6)

	e.recvXorKey = xorKey
	return nil
}
